{
	setCaption( tr( "Segments" ) );
	segmentTabs->changeTab( volume, tr( "Volume" ) );
	segmentTabs->changeTab( color, tr( "Color" ) );
	fileNewAction->setText( tr( "New" ) );
	fileNewAction->setMenuText( tr( "&New" ) );
	fileNewAction->setToolTip( tr( "create new segment" ) );
	fileNewAction->setAccel( tr( "Ctrl+N" ) );
	fileOpenAction->setText( tr( "Open" ) );
	fileOpenAction->setMenuText( tr( "&Open..." ) );
	fileOpenAction->setToolTip( tr( "open segment" ) );
	fileOpenAction->setAccel( tr( "Ctrl+O" ) );
	fileSaveVisibleAction->setText( tr( "Save" ) );
	fileSaveVisibleAction->setMenuText( tr( "&Save" ) );
	fileSaveVisibleAction->setToolTip( tr( "save visible segments" ) );
	fileSaveVisibleAction->setAccel( tr( "Ctrl+S" ) );
	editCutAction->setText( tr( "Cut" ) );
	editCutAction->setMenuText( tr( "Cu&t" ) );
	editCutAction->setToolTip( tr( "delete segment" ) );
	editCutAction->setAccel( tr( "Ctrl+X" ) );
	editCopyAction->setText( tr( "Copy" ) );
	editCopyAction->setMenuText( tr( "&Copy" ) );
	editCopyAction->setToolTip( tr( "copy segment (inactiv)" ) );
	editCopyAction->setAccel( tr( "Ctrl+C" ) );
	fileSaveAllAction->setText( tr( "Save All" ) );
	fileSaveAllAction->setMenuText( tr( "Save &All" ) );
	fileSaveAllAction->setToolTip( tr( "save all segments" ) );
	fileSaveAllAction->setAccel( tr( "Ctrl+A" ) );
	segmentUpAction->setText( tr( "Segment up" ) );
	segmentUpAction->setToolTip( tr( "Move the segment up" ) );
	segmentUpAction->setStatusTip( tr( "Move the segment up" ) );
	segmentUpAction->setWhatsThis( tr( "<b>Moves</b> the selected segment one step <b>up</b> in the segment list." ) );
	segmentDownAction->setText( tr( "Segment down" ) );
	segmentDownAction->setToolTip( tr( "Move the segment down" ) );
	segmentDownAction->setWhatsThis( tr( "<b>Moves</b> the segment one step <b>down</b> in the segment list." ) );
	toolBar->setLabel( tr( "Segment tools" ) );
}

